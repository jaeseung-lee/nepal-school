export function getVisa(slug: string): Visa {
  const visa = VISAS.find((v) => v.slug === slug);
  if (!visa) throw new Error(`등록되지 않은 비자 slug: ${slug}`);
  return visa;
}

export function visasByCountry(country: Visa["country"]): Visa[] {
  return VISAS.filter((v) => v.country === country);
}

export function buildVisaMetadata(slug: string): Metadata {
  const visa = getVisa(slug);
  return {
    ...buildPageMetadata({
      title: visa.seoTitle,
      description: visa.summary,
      path: `/visa/${visa.slug}`,
    }),
    keywords: visa.keywords,
  };
}
