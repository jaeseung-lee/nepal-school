export async function getSalesLocale(): Promise<SalesLocale> {
  const value = (await cookies()).get(SALES_LOCALE_COOKIE)?.value;
  return value === "ko" ? "ko" : "ja";
}

export function formatSalesDate(value: string | null | undefined, locale: SalesLocale) {
  if (!value) return "—";
  const date = new Date(value);
  if (Number.isNaN(date.valueOf())) return "—";
  return new Intl.DateTimeFormat(locale === "ja" ? "ja-JP" : "ko-KR", {
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  }).format(date);
}
